public class JavaVariables {
    public static void main(String[] args) {
        //Existen muchos tipos de variables
        int myNumero1 = 5;
        int myNumero2 = 5;//Se pueden declarar mas de una variable
        float myFloatNumero = 5.99f;
        char myLetra = 'D';
        boolean myBooleano = true;
        String myTexto = "Hola";
        System.out.println(myTexto + "Estudiante");//Se puede combinar variables y texto en un println()
    }
}
